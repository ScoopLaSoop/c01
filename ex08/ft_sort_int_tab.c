void	ft_sort_int_tab(int *tab, int size)
{
	int	x;
	int	y;

	x = 0;
	while ((x + 1) < size)
	{
		if (tab[x] > tab[x + 1])
		{
			y = tab[x];
			tab[x] = tab[x + 1];
			tab[x + 1] = y;
			x = 0;
		}
		else
			x++;
	}
}
